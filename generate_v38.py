# 4. 为每条方剂标注证型标签
# ============================================================

def match_syndrome(p):
    """为方剂匹配证型标签"""
    cure = p.get('cure', '')
    effect = p.get('effect', '')
    category = p.get('category', '')
    text = cure + ' ' + effect + ' ' + category

    matched = []
    for syndrome, info in SYNDROMES.items():
        score = 0
        symptoms = info['symptoms']
        for s in symptoms:
            if s in text:
                score += 2  # 每个症状匹配+2分
        # category_hint匹配+3分
        if info['category_hint'] and info['category_hint'] in category:
            score += 3
        if score >= 3:  # 至少3分（1个症状+category匹配 或 2个症状）
            matched.append((syndrome, score, info['desc']))

    # 按匹配度排序，最多保留5个；阈值1个症状即匹配
    matched.sort(key=lambda x: -x[1])
    return [m[0] for m in matched[:5] if m[1] >= 1]

